emarks= int(input("Enter your marks of english out of 100 - "))

if(emarks <= 10 and emarks > 0):
    print("You are failed in english")
elif(emarks > 10 and emarks <= 25):
    print("You got E grade in english")
elif(emarks > 25 and emarks <= 45):
    print("You got D grade in english")
elif(emarks > 45 and emarks <= 65):
    print("You got C grade in english")
elif(emarks > 65 and emarks <= 85):
    print("You got B grade in english")
elif(emarks > 85 and emarks <= 100):
    print("You got A grade in english")
else:
    print("Enter valid marks")

